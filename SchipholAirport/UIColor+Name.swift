import UIKit

//  MARK: UIColor+Name
/// Extension to retrieve all colors by name.
///
/// Colors are inside the Assets.xcassets
///
extension UIColor {

  static let background$ = UIColor(named: "background$")

  static let accentColor$ = UIColor(named: "accentColor$")
}
